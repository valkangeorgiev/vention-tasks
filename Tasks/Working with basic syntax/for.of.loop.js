                                                            // We use for of loop to iterate through values of an iterable (it can be arrays, strings, etc.)
let newCars = ['Audi','BMW', 'Toyota', 'Honda', 'Mazda']
 function GetArrayValues(){
    for ( car of newCars) {
        console.log(car);
    }
 }

GetArrayValues();

let newCar = 'Audi';
 function GetStringValues(){
    for ( char of newCar) {
        console.log(char);
    }
 }

GetStringValues();

 


 
